    emit_progress(progress, "sfao validate", 4, total_steps, "checking YAML files")
    validate_yaml_files(root, result)
    emit_progress(progress, "sfao validate", 5, total_steps, "checking TOML files")
    validate_toml_files(root, result)
    emit_progress(progress, "sfao validate", 6, total_steps, "checking JSON files")
    validate_json_files(root, result)
    emit_progress(progress, "sfao validate", 7, total_steps, "compiling Python files")
    validate_python(root, result)
    emit_progress(progress, "sfao validate", 8, total_steps, "checking text shape and newlines")
    validate_text_shape(root, result)
    emit_progress(progress, "sfao validate", 9, total_steps, "checking trigger evals")
    validate_trigger_evals(root, result)
    emit_progress(progress, "sfao validate", 10, total_steps, "checking generated agent adapters")
    validate_generated_sync(root, result)
    emit_progress(progress, "sfao validate", 11, total_steps, "checking Copilot and release guardrails")
    validate_copilot_apply_to(root, result)
    validate_release_workflow(root, result)
    validate_prohibited_references(root, result)
    emit_progress(progress, "sfao validate", 12, total_steps, "checking Salesforce metadata guardrails")
    validate_salesforce_metadata(root, result)
