def validate_installation(
    root: Path,
    expected_version: str | None = None,
    progress: ProgressCallback | None = None,
) -> ValidationResult:
    root = root.resolve()
    result = ValidationResult()
    found_any = False
    total_steps = 5
    emit_progress(progress, "sfao validate", 1, total_steps, "checking installed file inventory")
    for relative in INSTALL_REQUIRED_FILES:
        path = root / relative
        if path.exists():
            found_any = True
        else:
            result.warn(f"Missing installed file: {relative}")
    if not found_any:
        result.error("No Salesforce Agent Optimizer installation files found")
        return result
    emit_progress(progress, "sfao validate", 2, total_steps, "checking installed skill versions")
    if expected_version:
        for relative in (
            ".agents/skills/salesforce-agent-optimizer/SKILL.md",
            ".claude/skills/salesforce-agent-optimizer/SKILL.md",
            ".github/skills/salesforce-agent-optimizer/SKILL.md",
        ):
            path = root / relative
            if path.exists():
                validate_skill_file(path, result, expected_version, require_all_compatibility=False)
    emit_progress(progress, "sfao validate", 3, total_steps, "checking generated markers")
    marker_files = [
        ".agents/skills/salesforce-agent-optimizer/SKILL.md",
        ".claude/skills/salesforce-agent-optimizer/SKILL.md",
        "AGENTS.md",
        ".github/skills/salesforce-agent-optimizer/SKILL.md",
        ".github/copilot-instructions.md",
        ".github/instructions/salesforce-agent-optimizer.instructions.md",
    ]
    for relative in marker_files:
        path = root / relative
        if path.exists() and GENERATED_MARKER not in read_text(path):
            result.error(f"Generated marker missing from installed file: {relative}")
    emit_progress(progress, "sfao validate", 4, total_steps, "checking installed YAML and text files")
    validate_installed_yaml(root, result)
    validate_installed_text_shape(root, result)
    emit_progress(progress, "sfao validate", 5, total_steps, "checking Salesforce metadata guardrails")
    validate_salesforce_metadata(root, result)
    return result


def validate_installed_yaml(root: Path, result: ValidationResult) -> None:
    for relative in INSTALL_REQUIRED_FILES:
        path = root / relative
        if not path.exists() or path.suffix.lower() not in YAML_SUFFIXES:
            continue
        text = read_text(path)
        if yaml is not None:
            try:
                yaml.safe_load(text)
            except Exception as exc:  # noqa: BLE001
                result.error(f"Invalid YAML {relative}: {exc}")
        elif "\t" in text:
            result.error(f"Invalid YAML {relative}: tab indentation is not supported")


def validate_installed_text_shape(root: Path, result: ValidationResult) -> None:
    for relative in INSTALL_REQUIRED_FILES:
        path = root / relative
        if not path.exists() or path.suffix.lower() not in TEXT_SUFFIXES | YAML_SUFFIXES:
            continue
        data = path.read_bytes()
        try:
            text = data.decode("utf-8")
        except UnicodeDecodeError as exc:
            result.error(f"File is not UTF-8: {relative}: {exc}")
            continue
        if data and not data.endswith(b"\n"):
            result.error(f"Missing final newline: {relative}")
        if b"\r\n" in data:
            result.error(f"CRLF newline found; expected LF: {relative}")
        if len(text) > 300 and len(text.splitlines()) <= 1:
            result.error(f"File appears collapsed into one line: {relative}")


def validate_auto(
    root: Path,
    expected_version: str | None = None,
    progress: ProgressCallback | None = None,
) -> ValidationResult:
    if (root / "SKILL.md").exists() and (root / "pyproject.toml").exists():
        return validate_source_tree(root, progress=progress)
    return validate_installation(root, expected_version=expected_version, progress=progress)
